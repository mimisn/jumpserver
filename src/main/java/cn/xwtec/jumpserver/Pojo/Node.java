package cn.xwtec.jumpserver.Pojo;

public class Node {
private String id;
private String key;
private String value;

public void setId(String id){
this.id = id;
}
public String getId(){
return this.id;
}
public void setKey(String key){
this.key = key;
}
public String getKey(){
return this.key;
}
public void setValue(String value){
this.value = value;
}
public String getValue(){
return this.value;
}

}